public class Unary {
  public static void main(String[] args) {
    int a = 10;
    int b = 5;

    System.out.printf("a = %d\n", a);
    System.out.printf("b = %d\n", b);

    System.out.printf("a++ = %d\n", a++);
    System.out.printf("++b = %d\n", ++b);

    System.out.printf("a-- = %d\n", a--);
    System.out.printf("--b = %d\n", --b);

    System.out.printf("a = %d\n", a);
    System.out.printf("b = %d\n", b);
  }
}
